package yr.java.twoback;

public class H011_LiteralTest {
	
	public static void main(String[] args) {
		
		System.out.println("Hello\nMy name is Yura Lim");
		//hello
		//my name is Yura Lim
		
		System.out.println("Its\'my book");
		//It's my book
		
		System.out.println("Hucks\b high");
		//Huck high
		System.out.println("이 표시(\\)를 보면 4를 누르시오.");
		//이 표시(\)를 보면 4를 누르시오 
		 
		System.out.println("예를 들어 \"를 사용할 수 있다.");
		//예를 들어 "를 사용할 수 있다. 
		
		System.out.println(0xFA); //250 16진수
		System.out.println(0222); //146 8진수
		
	}

}
